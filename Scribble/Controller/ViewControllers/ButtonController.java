package Scribble.Controller.ViewControllers;

/**
 * The methods in this class will be called by the views when the various buttons are pressed.
 * @author Peter Schinske
 */
public class ButtonController {

    /**
     * This method gets called when the "Finish Turn" button is pressed.
     */
    public static void finishTurnButtonAction() {
        System.out.println("Finish turn button pressed");
    }

    /**
     * This method gets called when the "Reset Turn" button is pressed.
     */
    public static void resetTurnButtonAction() {
        System.out.println("Reset turn button pressed");
    }

    /**
     * This method gets called when the "Randomize Letters" button is pressed.
     */
    public static void randomizeLettersButtonAction() {
        System.out.println("Randomize letters button pressed");
    }

    /**
     * This method gets called when the "Replace Letters" button is pressed.
     */
    public static void replaceLettersButtonAction() {
        System.out.println("Replace letters button pressed");
    }

    /**
     * This method gets called when the "Replace Letters" button is pressed.
     */
    public static void challengeButtonAction() {
        System.out.println("Challenge button pressed");
    }

    /**
     * This method gets called when the "Accept" button is pressed.
     */
    public static void acceptButtonAction() {
        System.out.println("Accept button pressed");
    }
}
